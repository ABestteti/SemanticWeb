<h2>Projeto - OntoMÚsica </h2>
<p style="text-align: justify;">A Ontomúsica é uma ontologia sobre a história da música codificada OWL. Através dela sistemas de  busca, sistemas inteligentes, estudiosos e/ou interessados no tema  podem ter acesso a conceitos, e seus relacionamentos semânticos, da  história musical, como por exemplo gêneros musicais, obras e autores.  Esse trabalho foi orientado por Rodrigo Goulart e desenvolvido pelo aluno <a href="mailto:rogerio.boff@gmail.com">Rogério Boff</a> em seu trabalho   de conclusão de curso. </p>
