<h2>Projeto - Ontolp Plugin</h2>
<p style="text-align: justify;">O projeto Ontolp foi um
trabalho de dissertação de mestrado em computação
aplicada. Nesse trabalho foi desenvolvido um plugin que serve de
auxílio
à engenharia de ontologias com base em corpus anotado da língua
portuguesa. Este trabalho foi desenvolvido por Luiz Carlos Ribeiro
Junior (lucarijr@gmail.com) e orientado pela professora Renata Vieira
(renata.vieira@pucrs.br).</p>
