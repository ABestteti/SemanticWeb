<h2>Agrissolos - Sistema de Informação sobre os Solos do Brasil</h2>
<p style="text-align: justify;"> O Agrissolos é um sistema de informação que visa suprir o zoneamento agrícola, a previsão de safras e outros processos de apoio à tomada de decisão na agricultura com informação consolidada e conhecimento útil sobre os solos brasileiros. A arquitetura do Agrissolos contempla três módulos principais além da interface Web: (i) um banco de dados de perfis de solos, cobrindo todo o território brasileiro; (ii) uma base de conhecimento, com descrições de solos para fins de uso agrícola, na forma de ontologias e árvores de decisão e (iii) um sistema de informação geográfica, para a manipulação de informação cartográfica e apresentação gráfica do conhecimento. </p>
