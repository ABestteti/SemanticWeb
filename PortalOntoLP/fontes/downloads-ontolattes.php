<h2>Ontologia de Currículo Lattes</h2>
<p style="text-align: justify;">O Projeto "Populando ontologias através de informações em HTML - o caso do currículo lattes" foi um trabalho de dissertação de mestrado de André Casado Castaño (casado@linux.ime.usp.br) orientado pela
Profa. Dra. Renata Wassermann (renata@ime.usp.br). Nesse trabalho foi
utilizada como fonte de informações currículos da Plataforma Lattes
para popular uma ontologia e utilizá-la principalmente como uma base
de dados a ser consultada para geração de relatórios. Todo processo de
extração de informações foi realizado a partir de arquivos HTML, com
processamento posterior para inserção correta dentro da ontologia, de
acordo com sua semântica.</p>

