<h2>Projeto - Ontolp Lista de Termos</h2>
<p style="text-align: justify;">Um dos trabalhos desenvolvidos no contexto do projeto OntoLP foi a realização de experimentos de extração de termos compostos, candidatos a conceitos, a partir de um corpus da área da pediatria.
Esse corpus é composto por 283 textos (785.448 palavras) em português extraídos do <a href="http://www.jped.com.br/" target="_blank">Jornal de Pediatria </a>. O corpus foi organizado por Coulthard para estudar padrões de tradução português-inglês (COULTHARD, 2005).<br/>

No contexto do projeto TEXTQUIM/TEXTECC da Universidade Federal do Rio Grande do Sul, duas listas de termos compostos  foram construída a partir desse corpus. As listas constituem um catálogo e um dicionário de termos. O propósito do dicionário (disponível em <a href="http://www6.ufrgs.br/textecc/" target="_blank">www.ufrgs.br/textecc</a>) é dar apoio aos estudantes de tradução nesse domínio.<br/>

No contexto do projeto OntoLP, essas listas iniciais foram unidas e revisadas. O resultado final deste processo foi uma lista com 2151 termos compostos, sendo 1421 bi-gramas e 730 tri-gramas. Termos de composição maior que 3 palavras não foram incluídos. Essa lista revisada tem sido utilizada como lista de referência em experimentos de extração de termos.</p>

