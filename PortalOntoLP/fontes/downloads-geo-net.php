<h2>Geo-Net-PT01 - a primeira ontologia geográfica de Portugal</h2>
<p style="text-align: justify;">A Geo-Net-PT01 é uma ontologia geográfica pública de Portugal. Foi criada pelo Grupo XLDB, da Faculdade de Ciências da Universidade de Lisboa, no âmbito do projecto GREASE (Raciocínio Geográfico para Motores de Busca).
A Geo-Net-PT01 contém 418.065 dados geográficos administrativos de Portugal, com informação sobre os distritos, municípios e ruas, entre outros dados. Além da informação geográfica, inclui o âmbito geográfico atribuído a 686 sites da Internet.</p>
