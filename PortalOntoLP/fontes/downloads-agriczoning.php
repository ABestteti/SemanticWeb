<h2>Projeto - A abordagem POESIA para integração de dados e serviços da Web Semântica</h2>
<p style="text-align: justify;"> Esse projeto foi a tese de doutorado de <a href="http://www.inf.ufsc.br/~fileto/" target="_blank">Renato Fileto</a>. <br /> POESIA (Processes for Open-Ended Systems for Information Analysis) tem por objetivo a construção de processos complexos envolvendo integração e análise de dados de diversas fontes, particularmente em aplicações científicas. Sua abordagem é centrada em dois tipos de mecanismos da Web Semântica: workflows científicos, para especificar e compor serviços Web; e ontologias de domínio, para viabilizar a interoperabilidade e o gerenciamento semântico dos dados e processos. </p>



